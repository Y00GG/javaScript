'use strict'


// Switch

var edad = 18;

var imprime = "";


switch(edad){
    case 18:
        console.log(imprime = "Acabas de cumplir la mayoria de edad");
        break;
    case 25:
        console.log(imprime = "Ya eres un adulto");
        break;
    case 40:
        console.log(imprime = "Crisis de los 40");
        break;
    case 75:
        console.log(imprime = "Ya eres un anciano");
        break;
    default:
        console.log(imprime = "tu edad es neutra");
        break;
}